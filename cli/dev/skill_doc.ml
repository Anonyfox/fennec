let render () =
  String.concat "\n"
    [ "# Fennec";
      "";
      "Fennec is an OCaml fullstack framework built on plain dune projects. Use the CLI as the source of truth for builds, dev feedback, tests, docs, and agent fastlane feedback.";
      "";
      "## Start Here";
      "";
      "```sh";
      "fennec                 # print this guide";
      "fennec dev             # discover and run the app with live reload";
      "fennec test            # fast unit gate";
      "fennec test all        # unit -> http -> browser -> system -> docs";
      "fennec test docs       # doc coverage, warn-only by default";
      "fennec build INPUT...  # native JS/CSS/SCSS bundling";
      "```";
      "";
      "The project remains a normal dune project. `fennec` adds restart/livereload, native asset bundling, app test orchestration, doc coverage, and agent-facing feedback.";
      "";
      "## Dev Loop";
      "";
      "Run:";
      "";
      "```sh";
      "fennec dev";
      "```";
      "";
      "With no server path, Fennec asks dune for the executable that calls `Fennec.serve`. Dune remains the sole source watcher and builder. Backend changes restart the server; frontend changes reload or hot-swap CSS; build failures keep the last good server serving when possible.";
      "";
      "Useful variants:";
      "";
      "```sh";
      "fennec dev --dry-run";
      "fennec dev --port 9000";
      "fennec dev --mongo";
      "fennec dev --clean";
      "```";
      "";
      "## Agent Fastlane";
      "";
      "For coding agents, prefer the evented fastlane over manual `dune build` / `dune runtest` probes after edits.";
      "";
      "Start the dev loop with an agent journal:";
      "";
      "```sh";
      "fennec dev --agent --port 9123";
      "```";
      "";
      "Attach edit hooks to:";
      "";
      "```sh";
      "fennec agent hook --timeout 12";
      "```";
      "";
      "If the harness supports pre-tool hooks, run this before edit/write tools:";
      "";
      "```sh";
      "fennec agent mark";
      "```";
      "";
      "Then keep `fennec agent hook --timeout 12` as the post-tool or post-batch hook. The hook blocks on the devserver journal and injects the completed dev verdict into the next model step. Do not parse terminal output. Do not run an extra build/test command after every edit when the hook is active.";
      "";
      "The agent verdict includes served change, inline-test result when tests ran, inferred affected surface, timing fields, and focused compiler diagnostics with file/line/code frame when a build fails.";
      "";
      "If hooks are not available, use exactly one wait after an edit batch:";
      "";
      "```sh";
      "fennec agent wait --timeout 12";
      "```";
      "";
      "Recovery:";
      "";
      "```sh";
      "fennec agent status";
      "```";
      "";
      "`status` reports the journal, latest verdict, configured port, and whether the recorded devserver pid is alive.";
      "";
      "## Tests";
      "";
      "```sh";
      "fennec test              # unit";
      "fennec test http";
      "fennec test browser";
      "fennec test system";
      "fennec test docs --strict";
      "fennec test new system NAME";
      "```";
      "";
      "Use the narrowest cut that proves the change. `all` is the full local confidence pass.";
      "";
      "## Agent Rules";
      "";
      "- Prefer `fennec dev --agent` plus hooks for iterative coding.";
      "- Trust hook verdicts for compile/test/reload feedback; they are generated by the devserver, not by terminal scraping.";
      "- On build failure, fix the reported focused diagnostic first.";
      "- Use one manual `fennec agent wait` only when hooks cannot fire.";
      "- Avoid adding local harness config or root instruction files unless the user explicitly asks.";
      "- Before final handoff, run the relevant `fennec test ...` cut or explain why it was not run.";
      "" ]

let%test "guide names the agent hook" =
  Fennec_hunt_unit.str_contains (render ()) "fennec agent hook --timeout 12"

let%test "guide tells agents not to poll builds after hooks" =
  Fennec_hunt_unit.str_contains (render ()) "Do not run an extra build/test command after every edit"
